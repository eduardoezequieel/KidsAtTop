    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        MtoUsuario mto = new MtoUsuario();
        String usuario = mto.capturarUsuario(1);
        if (usuario.equals(jContraseña.getText().trim()) && usuario.equals(jConfirmarContraseña.getText().trim())) {
            JOptionPane.showMessageDialog(null, "No puedes poner tu usuario como contraseña.","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
        else if(jContraseña.getText().trim().isEmpty() || jConfirmarContraseña.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Campos vacios.","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
        else if(jContraseña.getText().trim().length() < 8 && jConfirmarContraseña.getText().trim().length() < 8)
        {
            JOptionPane.showMessageDialog(null, "Esta contraseña tiene menos de 8 caracteres, escriba una nueva.","Advertencia",JOptionPane.WARNING_MESSAGE);
        }
        else if(jContraseña.getText().trim().equals(jConfirmarContraseña.getText().trim()))
        {
            int mensaje = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que quieres esta contraseña?","Aviso",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE);
            if (mensaje == JOptionPane.YES_OPTION) {
                CtrlUsuario ctrl = new CtrlUsuario();
                String contraseña = jContraseña.getText();
                String contraseñaEncriptada = DigestUtils.sha1Hex(contraseña);
                ctrl.setContraseña(contraseñaEncriptada);
                ctrl.setId_usuario(1);

                if (mto.actualizarContraseña()) {
                    JOptionPane.showMessageDialog(null, "Contraseña guardada.","Exito",JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);
                    FrmPrimerUsoPreguntas form = new FrmPrimerUsoPreguntas();
                    form.setVisible(true);
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

