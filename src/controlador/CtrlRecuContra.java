package controlador;

/**
 *
 * @author katy0
 */
public class CtrlRecuContra {
    
    //Declarando variables 
    private static int codigoVerificacion;
    private static String correo;
    private static String usuario;
    private static String nuevaContra;
    private static String contraseña;
    private static String admin;

    /**
     * @return the codigoVerificacion
     */
    public int getCodigoVerificacion() {
        return codigoVerificacion;
    }

    /**
     * @param codigoVerificacion the codigoVerificacion to set
     */
    public void setCodigoVerificacion(int codigoVerificacion) {
        this.codigoVerificacion = codigoVerificacion;
    }

    /**
     * @return the correo
     */
    public String getCorreo() {
        return correo;
    }

    /**
     * @param correo the correo to set
     */
    public void setCorreo(String correo) {
        this.correo = correo;
    }

    /**
     * @return the usuario
     */
    public String getUsuario() {
        return usuario;
    }

    /**
     * @param usuario the usuario to set
     */
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    /**
     * @return the nuevaContra
     */
    public String getNuevaContra() {
        return nuevaContra;
    }

    /**
     * @param nuevaContra the nuevaContra to set
     */
    public void setNuevaContra(String nuevaContra) {
        this.nuevaContra = nuevaContra;
    }

    /**
     * @return the contraseña
     */
    public String getContraseña() {
        return contraseña;
    }

    /**
     * @param contraseña the contraseña to set
     */
    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }

    /**
     * @return the admin
     */
    public String getAdmin() {
        return admin;
    }

    /**
     * @param admin the admin to set
     */
    public void setAdmin(String admin) {
        this.admin = admin;
    }
}
